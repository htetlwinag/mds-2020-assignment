package triangle;

public class triangle {

  public static void main(String[] args) {
   int n=5;
for (int i=1;i<=n ;i++ ){
for (int j=1;j<=i ;j++ ){
System.out.print("*");
}
System.out.println( );
}
  }
}


// n ကို5ပေးထားတယ် forနဲ့loop ၂ခါပတ်ထားတယ် iတခါjတခါ
// iက ၁ကနေစပီး nအထိ တစ်တိုးပီးလုပ်မယ်
//jက ၁ကနေစပီး iအထိ တစ်တိုးပီးလုပ်တယ်